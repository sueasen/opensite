function computeUrl() {
  const randomNumber = Math.random();
  if (randomNumber < 0.35) {
    return computeUrlFePass();
  } else if (randomNumber < 0.805) {
    return computeUrlFeAp('fe');
  } else {
    return computeUrlFeAp('ap');
  }
}

function computeUrlFePass() {
  const qsKeys = Object.keys(qs);
  const qsKey = qsKeys[Math.floor(Math.random() * qsKeys.length)];
  const rate = (qs[qsKey].length - 1) / selectNum.value;
  const min = Math.floor((selectQ.value - 1) * rate + 1);
  const max = Math.floor(selectQ.value * rate);
  const num = Math.floor(Math.random() * (max - min + 1)) + min;
  const url = qs[qsKey][num];
  return url ? url : computeUrlFePass();
}

function computeUrlFeAp(type) {
  const season =
    seasons[type][Math.floor(Math.random() * seasons[type].length)];
  const rate = nums[nums.length - 1] / selectNum.value;
  const min = Math.floor((selectQ.value - 1) * rate + 1);
  const max = Math.floor(selectQ.value * rate);
  const num = Math.floor(Math.random() * (max - min + 1)) + min;
  const url = `https://www.${type}-siken.com/kakomon/${season}/q${num}.html`;
  return url;
}

function applyIFrame(url) {
  if (window.matchMedia('(width < 768px)').matches) {
    url = url.replace('/kakomon/', '/s/kakomon/');
  }
  if (iframe.src == url) return;
  iframe.src = url;
}
